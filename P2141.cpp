#include <algorithm>
#include <iostream>
using namespace std;

bool c[10010];

int main() {
    int n;
    cin >> n;
    int a[n];
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(a, a + n);
    n = (unique(a, a + n) - a);
    int ans = 0;
    int x, y, z;
    for (x = 0; x < n; x++) {
        for (y = x + 1; y < n - 1; y++) {
            for (z = y + 1; z < n; z++) {
                if (x != y && a[x] + a[y] == a[z] && !c[z]) {
                    ans++;
                    //cout << a[x] << ' ' << a[y] << ' ' << a[z] << endl;
                    c[z] = 1;
                }
            }
        }
    }
    cout << ans;
    return 0;
}
